"""Sprint 5.18c hotfix — SOST address format validators.

The canonical SOST address format is "sost1" + 40 lowercase hex
chars (= 45 total chars). It is defined in
``include/sost/address.h`` (``address_encode``) and produced by
``sost-cli newwallet`` and ``sost-cli getnewaddress``. Earlier
Trinity sprints used a bech32-shaped regex that rejected
legitimate addresses; this hotfix aligns Trinity with the
canonical encoding.

This test file:

1. Accepts the three real-world addresses the operator generated
   via ``sost-cli getnewaddress``.
2. Rejects pre-existing fake addresses that don't match the new
   regex (no leading ``q`` ornament, no characters outside
   ``[0-9a-f]``).
3. Verifies that the centralised validator in
   ``useful_compute_worker_address_map.py`` is the source of
   truth and is imported wherever an address is checked.
"""

from __future__ import annotations

import importlib.util
import re
import sys
from pathlib import Path

import pytest


REPO_ROOT = Path(__file__).resolve().parents[2]
SCRIPTS_DIR = REPO_ROOT / "scripts" / "trinity"


def _load(modname: str, path: Path):
    spec = importlib.util.spec_from_file_location(modname, path)
    mod = importlib.util.module_from_spec(spec)
    sys.modules[modname] = mod
    spec.loader.exec_module(mod)
    return mod


@pytest.fixture(scope="module")
def addr_mod():
    return _load(
        "ucwam",
        SCRIPTS_DIR / "useful_compute_worker_address_map.py",
    )


# Real addresses generated by sost-cli on the operator's VPS
# (Sprint 5.17 test wallet). They are 45 chars long, "sost1" +
# 40 lowercase hex.
REAL_ADDRESSES = (
    "sost10a262c66d8fdcb45bd21b073b7d8674dac542542",
    "sost14c5281aeaa7f7ca356f5b1b2e76e46ca8409a25e",
    "sost1ca9097d830b74495b95db9d779ad63c90579bc18",
)


@pytest.mark.parametrize("addr", REAL_ADDRESSES)
def test_real_sost_cli_addresses_accepted(addr_mod, addr):
    assert addr_mod.validate_address(addr) is None, (
        f"real sost-cli address {addr!r} rejected"
    )
    assert addr_mod._ADDRESS_RE.match(addr), (
        f"real sost-cli address {addr!r} did not match regex"
    )
    assert len(addr) == 45


def test_address_pattern_exported_constant(addr_mod):
    """The pattern is published as a module-level constant so
    schemas and sibling scripts can reuse it without re-stating
    the regex."""
    assert hasattr(addr_mod, "SOST_ADDRESS_PATTERN")
    assert addr_mod.SOST_ADDRESS_PATTERN == r"^sost1[0-9a-f]{40}$"


