def _read(p: Path) -> str:
    return p.read_text(encoding="utf-8")


def test_payment_proposal_uses_new_regex():
    src = _read(
        SCRIPTS_DIR / "useful_compute_payment_proposal.py"
    )
    assert "^sost1[0-9a-f]{40}$" in src
    # Old bech32-shaped regex must NOT linger anywhere.
    assert "[023456789acdefghjklmnpqrstuvwxyz]" not in src


def test_payment_draft_uses_new_regex():
    src = _read(
        SCRIPTS_DIR / "useful_compute_payment_draft.py"
    )
    assert "^sost1[0-9a-f]{40}$" in src
    assert "[023456789acdefghjklmnpqrstuvwxyz]" not in src


def test_worker_address_map_uses_new_regex():
    src = _read(
        SCRIPTS_DIR / "useful_compute_worker_address_map.py"
    )
    assert "^sost1[0-9a-f]{40}$" in src
    assert "[023456789acdefghjklmnpqrstuvwxyz]" not in src


def test_schemas_use_new_regex():
    for schema_name in (
        "useful_compute_payment_draft.schema.json",
        "useful_compute_payment_proposal.schema.json",
    ):
        p = REPO_ROOT / "schemas" / "trinity" / schema_name
        src = _read(p)
        assert "^sost1[0-9a-f]{40}$" in src, (
            f"{schema_name} did not adopt the new regex"
        )
        assert "[023456789acdefghjklmnpqrstuvwxyz]" not in src
        assert "[0-9a-z]{20,80}" not in src


