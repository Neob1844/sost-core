def test_address_map_accepts_real_addresses(tmp_path, addr_mod):
    import json
    obj = {
        "schema": addr_mod.SCHEMA_MAP,
        "workers": [
            {
                "worker_id_hash": f"{i:016x}",
                "payout_address": REAL_ADDRESSES[i],
                "label": f"real-worker-{i}",
            }
            for i in range(len(REAL_ADDRESSES))
        ],
    }
    p = tmp_path / "map.json"
    p.write_text(
        json.dumps(obj, sort_keys=True, separators=(",", ":")),
        encoding="utf-8",
    )
    problems = addr_mod.validate_map(obj)
    assert problems == [], f"unexpected problems: {problems}"


def test_address_map_rejects_truncated_real_address(
    tmp_path, addr_mod,
):
    obj = {
        "schema": addr_mod.SCHEMA_MAP,
        "workers": [{
            "worker_id_hash": "0" * 16,
            "payout_address":
                REAL_ADDRESSES[0][:-1],  # drop last hex char
            "label": "truncated",
        }],
    }
    problems = addr_mod.validate_map(obj)
    assert any("payout_address" in p for p in problems), (
        f"truncated address should be rejected; problems={problems}"
    )
