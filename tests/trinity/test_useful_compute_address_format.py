@pytest.mark.parametrize("addr,why", [
    ("",                                             "empty"),
    ("sost1",                                        "prefix only"),
    ("sost10a262c66d8fdcb45bd21b073b7d8674dac54254", "truncated by 1 char"),
    ("sost10a262c66d8fdcb45bd21b073b7d8674dac5425421", "one extra char"),
    ("sost10A262C66D8FDCB45BD21B073B7D8674DAC542542", "uppercase hex not allowed"),
    ("SOST10a262c66d8fdcb45bd21b073b7d8674dac542542", "uppercase prefix"),
    ("xost10a262c66d8fdcb45bd21b073b7d8674dac542542", "wrong prefix"),
    ("sost10a262c66d8fdcb45bd21b073b7d8674dac542g42", "non-hex char g"),
    ("sost10a262c66d8fdcb45bd21b073b7d8674dac542 42", "embedded space"),
    ("sost1qaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa", "bech32-shaped, leading q"),
    ("sost1xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx",   "template placeholder"),
])
def test_invalid_addresses_rejected(addr_mod, addr, why):
    assert addr_mod.validate_address(addr) is not None, (
        f"address {addr!r} should be rejected: {why}"
    )


def test_validate_address_returns_message_string(addr_mod):
    msg = addr_mod.validate_address("xost10a262c66d8fdcb45bd21b0")
    assert isinstance(msg, str)
    assert "payout_address" in msg


